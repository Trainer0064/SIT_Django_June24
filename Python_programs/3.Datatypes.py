"""
Datatypes are the classification of data items.

Built-in datatypes (already available)
User Defined Datatypes (created by the user)
"""

"""
int
float
complex
str
bool
list - array
tuple
set
dictionary

ordered collection
"""

i = 10
print(i,type(i))
f = 10.21
print(f,type(f))
c = 10+20j
print(c,type(c))
s = "SIT"
print(s,type(s))
b = True
print(b,type(b))
l = [10,20,30]
print(l,type(l))
t = (10,20,30)
print(t,type(t))
s = {10,20,30}
print(s,type(s))
d = {10:100,20:200,30:300}
print(d,type(d))

n = None #nothing
print(n,type(n))